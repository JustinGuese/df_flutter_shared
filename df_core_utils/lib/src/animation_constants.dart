/// Centralized animation durations for reuse across apps.
abstract class AnimationDurations {
  static const fast = Duration(milliseconds: 150);
  static const normal = Duration(milliseconds: 300);
  static const medium = Duration(milliseconds: 400);
  static const slow = Duration(milliseconds: 500);
  static const pulse = Duration(milliseconds: 1000);
  static const emphasis = Duration(milliseconds: 2000);
}
